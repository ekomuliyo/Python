# Slicing List
listmu = [1, 2, 3, 4, 6, 7, 8, 9, 10]
listA = listmu[2:6] # mengambil nilai dari index ke 2 sampai index 6 - 1
listB = listmu[3:] # mengambil nilai mulai dari index 3 sampai seterusnya
listC = listmu[:3] # mengambil nilai dari index pertama sampai index ke 3
print(f'listku {listA}')
print(f'listku {listB}')
print(f'listku {listC}')

